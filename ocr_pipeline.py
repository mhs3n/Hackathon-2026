from pydantic import BaseModel, validator
from typing import Optional

class KPISchema(BaseModel):
    institution:          str
    source_file:          str
    taux_reussite:        Optional[float] = None
    taux_presence_s1:     Optional[float] = None
    taux_presence_s2:     Optional[float] = None
    taux_abandon:         Optional[float] = None
    taux_redoublement:    Optional[float] = None
    moyenne_generale:     Optional[float] = None
    budget_alloue:        Optional[float] = None
    budget_consomme:      Optional[float] = None
    cout_par_etudiant:    Optional[float] = None
    consommation_energie: Optional[float] = None
    empreinte_carbone:    Optional[float] = None

    @validator('taux_reussite','taux_presence_s1','taux_presence_s2',
               'taux_abandon','taux_redoublement', pre=True)
    def pct(cls, v):
        if v is None: return None
        v = float(v)
        return round(v, 1) if 0 <= v <= 100 else None

    @validator('moyenne_generale', pre=True)
    def grade(cls, v):
        if v is None: return None
        v = float(v)
        return round(v, 2) if 0 <= v <= 20 else None

def validate_kpis(raw: dict, institution: str, source_file: str) -> KPISchema:
    raw.update({'institution': institution, 'source_file': source_file})
    return KPISchema(**raw)

def check_alerts(rec: KPISchema) -> list:
    alerts = []
    if rec.taux_reussite       is not None and rec.taux_reussite < 50:
        alerts.append(f'🔴 CRITIQUE — Taux de reussite: {rec.taux_reussite}% (seuil 50%)')
    if rec.taux_redoublement   is not None and rec.taux_redoublement > 20:
        alerts.append(f'🟠 ALERTE  — Taux de redoublement: {rec.taux_redoublement}%')
    if rec.moyenne_generale    is not None and rec.moyenne_generale < 10:
        alerts.append(f'🔴 CRITIQUE — Moyenne generale: {rec.moyenne_generale}/20')
    if rec.taux_presence_s1    is not None and rec.taux_presence_s1 < 75:
        alerts.append(f'🟠 ALERTE  — Presence S1 faible: {rec.taux_presence_s1}%')
    if rec.taux_presence_s2    is not None and rec.taux_presence_s2 < 75:
        alerts.append(f'🟠 ALERTE  — Presence S2 faible: {rec.taux_presence_s2}%')
    return alerts


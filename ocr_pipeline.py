def extract_kpis_regex(text: str) -> dict:
    results = {}
    tl = normalize_for_regex(text)
    print(f'\n   📝 Normalized text for regex:\n{tl}\n')
    for kpi, patterns in KPI_PATTERNS.items():
        found = None
        for p in patterns:
            m = re.search(p, tl, re.IGNORECASE | re.MULTILINE)
            if m:
                try:
                    raw   = m.group(1).strip().replace(' ', '').replace(',', '.')
                    found = float(raw)
                    # Sanity: percentages must be 0-100
                    if kpi in ('taux_reussite','taux_presence_s1','taux_presence_s2',
                               'taux_abandon','taux_redoublement') and found > 100:
                        found = None
                        continue
                except:
                    pass
                if found is not None:
                    break
        results[kpi] = found
        print(f"   {'✅' if found is not None else '❌'} {kpi}: {found}")
    return results


def extract_kpis_dataframe(data: pd.DataFrame) -> dict:
    """Compute KPIs directly from student-level DataFrame rows"""
    kpis = {}
    cols_lower = {c.lower(): c for c in data.columns}

    def find_col(*keywords):
        for kw in keywords:
            for cl, co in cols_lower.items():
                if kw in cl: return co
        return None

    res_col = find_col('resultat', 'outcome')
    if res_col:
        kpis['taux_reussite'] = round(
            data[res_col].astype(str).str.lower().isin(['admis']).sum() / len(data) * 100, 1)

    red_col = find_col('redoublement')
    if red_col:
        kpis['taux_redoublement'] = round(
            (data[red_col].astype(str).str.lower() == 'oui').sum() / len(data) * 100, 1)

    for sem in ['s1', 's2']:
        abs_col = find_col(f'absences {sem}')
        tot_col = find_col(f'seances totales {sem}', f'total {sem}')
        if abs_col and tot_col:
            pres = (data[tot_col] - data[abs_col]) / data[tot_col] * 100
            kpis[f'taux_presence_{sem}'] = round(pres.mean(), 1)

    avg_col = find_col('moyenne generale', 'moyenne gen')
    if avg_col:
        kpis['moyenne_generale'] = round(data[avg_col].mean(), 2)

    # Print extracted KPIs for visibility
    for k, v in kpis.items():
        print(f"   {'✅' if v is not None else '❌'} {k}: {v}")

    return kpis



import re

def normalize_for_regex(text: str) -> str:
    t = text.lower()

    # 1. Fix special OCR characters
    t = t.replace('œ', 'e').replace('æ', 'e')
    t = re.sub(r"[''`´]", "'", t)

    # 2. Fix "S1"/"S2" BEFORE digit rules (OCR reads S as 5)
    t = re.sub(r'\b51\b', 's1', t)
    t = re.sub(r'\b52\b', 's2', t)

    # 3. Collapse spaces/tabs — keep newlines
    t = re.sub(r'[ \t]+', ' ', t)

    # 4. Fix spaced decimals "72 . 3" → "72.3"
    t = re.sub(r'(\d)\s*\.\s*(\d)', r'\1.\2', t)

    # 5. Fix missing dot in 3-digit % values: "725%" → "72.5%"
    t = re.sub(r'\b([1-9]\d)(\d)\s*%', r'\1.\2%', t)

    # 6. Fix "moyenne" split across lines with value on next line
    t = re.sub(r'\b(\d{2})(\d{2})\s*\n\s*20\b', r'\1.\2 / 20', t)
    t = re.sub(r'(\d{1,2}[.,]\d{1,2})\s*\n\s*20\b', r'\1 / 20', t)

    # 7. Add space between letter and digit (skip s1/s2 context)
    t = re.sub(r'([a-rt-z%])(\d)', r'\1 \2', t)
    t = re.sub(r'(s)([3-9])', r'\1 \2', t)

    # 8. Handle thousands separators
    t = re.sub(r'(\d{1,3})([ ,])(\d{3})([ ,])(\d{3})', r'\1\3\5', t)
    t = re.sub(r'(\d{1,3})([ ,])(\d{3})(?!\d)', r'\1\3', t)

    # 9. Strip Arabic characters
    t = re.sub(r'[\u0600-\u06FF]+', ' ', t)

    # 10. Final space collapse

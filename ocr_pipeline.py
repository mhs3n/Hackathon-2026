def extract_from_pdf(path):
    """Digital PDFs — handles inline text + tables"""
    text = ''
    with pdfplumber.open(path) as pdf:
        for page in pdf.pages:
            page_text = page.extract_text(x_tolerance=3, y_tolerance=3) or ''
            # Also extract tables — institutions often export Word tables to PDF
            for table in (page.extract_tables() or []):
                for row in table:
                    clean_row = [str(c).strip() if c else '' for c in row]
                    if len(clean_row) == 2:
                        # "Label | Value" rows → convert to "label : value" for regex
                        page_text += f"\n{clean_row[0]} : {clean_row[1]}"
                    else:
                        page_text += '\n' + ' | '.join(clean_row)
            text += page_text + '\n'
    return text

def preprocess_for_ocr(img_array):
    """Grayscale + denoise + threshold + upscale — improves OCR on real scans"""
    gray     = cv2.cvtColor(img_array, cv2.COLOR_BGR2GRAY) if len(img_array.shape) == 3 else img_array
    denoised = cv2.fastNlMeansDenoising(gray, h=10)
    _, thresh = cv2.threshold(denoised, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
    scaled   = cv2.resize(thresh, None, fx=2, fy=2, interpolation=cv2.INTER_CUBIC)
    return scaled

def run_ocr(img_array):
    """EasyOCR on a preprocessed numpy image array"""
    import easyocr
    reader  = easyocr.Reader(['fr', 'en'], gpu=False)
    results = reader.readtext(img_array, detail=1)
    # Sort detections top-to-bottom, left-to-right
    results.sort(key=lambda r: (round(r[0][0][1] / 15) * 15, r[0][0][0]))
    raw = '\n'.join(r[1] for r in results)
    # Normalize OCR noise
    raw = re.sub(r"[''`´]", "'", raw)
    raw = re.sub(r'(\d)\s*[.,]\s*(\d)', r'\1.\2', raw)
    raw = re.sub(r'[ \t]+', ' ', raw)
    print(f"   🔍 OCR output ({len(raw)} chars):\n{raw[:600]}\n")
    return raw

def extract_from_image(path):
    """Image files — preprocess then OCR"""
    img_array = cv2.imread(path)
    processed = preprocess_for_ocr(img_array)
    return run_ocr(processed)

def extract_from_scanned_pdf(path):
    """Scanned PDFs — render each page as image then OCR"""
    doc = fitz.open(path)
    full_text = ''
    for page_num, page in enumerate(doc):
        mat = fitz.Matrix(2.5, 2.5)   # ~180 DPI — high enough for OCR
        pix = page.get_pixmap(matrix=mat, colorspace=fitz.csGRAY)
        img = Image.open(io.BytesIO(pix.tobytes("png")))
        img_array = np.array(img)
        print(f"   📄 OCR on scanned page {page_num + 1}...")
        full_text += run_ocr(img_array) + '\n'
    return full_text

def extract_from_excel(path):
    """Excel — fuzzy column matching handles inconsistent real-world headers"""
    sheets = pd.read_excel(path, sheet_name=None)
    parts  = []
    for name, sdf in sheets.items():
        parts.append(f'=== Feuille: {name} ===')
        parts.append(sdf.to_string(index=False))
    return '\n'.join(parts)

def extract_text(path, ftype):
    if ftype == 'pdf_digital':    return extract_from_pdf(path)
    elif ftype == 'pdf_scanned':  return extract_from_scanned_pdf(path)
    elif ftype == 'image':        return extract_from_image(path)
    elif ftype in ('excel','csv'): return extract_from_excel(path)
    return ''


def run_pipeline(file_path: str, institution: str) -> dict:
    print(f'📂 File: {os.path.basename(file_path)} | Institution: {institution}')

    ftype    = detect_file_type(file_path)
    raw_text = extract_text(file_path, ftype)
    print(f'   Type: {ftype} | Chars extracted: {len(raw_text)}')

    if ftype in ('excel', 'csv'):
        src_df   = pd.read_excel(file_path) if ftype == 'excel' else pd.read_csv(file_path)
        raw_kpis = extract_kpis_dataframe(src_df)
    else:
        raw_kpis = extract_kpis_regex(raw_text)

    validated = validate_kpis(raw_kpis, institution, os.path.basename(file_path))
    db_id     = save_to_db(validated, ftype, raw_text)
    alerts    = check_alerts(validated)

    print(f'   Saved as record #{db_id}')
    if alerts:
        for a in alerts: print(f'   {a}')
    else:
        print('   ✅ No alerts — all KPIs OK')

    return {'id': db_id, 'file_type': ftype, 'kpis': validated.dict(), 'alerts': alerts}

